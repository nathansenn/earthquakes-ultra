export interface GlobalVolcano {
  id: string;
  name: string;
  country: string;
  region: string;  // Geographic region
  subregion: string;
  latitude: number;
  longitude: number;
  elevation: number; // meters
  type: string; // Stratovolcano, Shield, Caldera, etc.
  status: 'active' | 'potentially_active' | 'holocene' | 'historical';
  lastEruption?: string; // Year or date
  vei?: number; // Volcanic Explosivity Index of last major eruption
  population10km?: number; // Population within 10km
  population30km?: number; // Population within 30km
  population100km?: number; // Population within 100km
  gvpId?: string; // Global Volcanism Program ID
}

