/**
 * Get all volcanoes for a specific country
 */
export function getVolcanoesByCountry(country: string): GlobalVolcano[] {
  return GLOBAL_VOLCANOES.filter(v => 
    v.country.toLowerCase() === country.toLowerCase()
  );
}

/**
 * Get all volcanoes in a region
 */
export function getVolcanoesByRegion(region: string): GlobalVolcano[] {
  return GLOBAL_VOLCANOES.filter(v => 
    v.region.toLowerCase().includes(region.toLowerCase())
  );
}

/**
 * Get volcano by ID
 */
export function getVolcanoById(id: string): GlobalVolcano | undefined {
  return GLOBAL_VOLCANOES.find(v => v.id === id);
}

/**
 * Get volcano by slug (URL-friendly name)
 */
export function getVolcanoBySlug(slug: string): GlobalVolcano | undefined {
  return GLOBAL_VOLCANOES.find(v => 
    v.name.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/(^-|-$)/g, '') === slug
  );
}

/**
 * Get volcanoes near a location
 */
export function getVolcanoesNearLocation(
  latitude: number,
  longitude: number,
  radiusKm: number
): GlobalVolcano[] {
  return GLOBAL_VOLCANOES.filter(v => {
    const dist = haversineDistance(v.latitude, v.longitude, latitude, longitude);
    return dist <= radiusKm;
  });
}

/**
 * Calculate haversine distance between two points
 */
function haversineDistance(lat1: number, lon1: number, lat2: number, lon2: number): number {
  const R = 6371; // Earth's radius in km
  const dLat = (lat2 - lat1) * Math.PI / 180;
  const dLon = (lon2 - lon1) * Math.PI / 180;
  const a = Math.sin(dLat/2) * Math.sin(dLat/2) +
            Math.cos(lat1 * Math.PI / 180) * Math.cos(lat2 * Math.PI / 180) *
            Math.sin(dLon/2) * Math.sin(dLon/2);
  const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
  return R * c;
}

/**
 * Get unique countries with volcanoes
 */
export function getVolcanoCountries(): string[] {
  return [...new Set(GLOBAL_VOLCANOES.map(v => v.country))].sort();
}

/**
 * Get unique regions
 */
export function getVolcanoRegions(): string[] {
  return [...new Set(GLOBAL_VOLCANOES.map(v => v.region))].sort();
}

/**
 * Get active volcanoes only
 */
export function getActiveVolcanoes(): GlobalVolcano[] {
  return GLOBAL_VOLCANOES.filter(v => v.status === 'active');
}

/**
 * Get volcanoes by status
 */
export function getVolcanoesByStatus(status: GlobalVolcano['status']): GlobalVolcano[] {
  return GLOBAL_VOLCANOES.filter(v => v.status === status);
}

/**
 * Get volcanoes sorted by population exposure
 */
export function getVolcanoesByPopulationRisk(): GlobalVolcano[] {
  return [...GLOBAL_VOLCANOES].sort((a, b) => {
    const popA = (a.population10km || 0) * 10 + (a.population30km || 0) * 3 + (a.population100km || 0);
    const popB = (b.population10km || 0) * 10 + (b.population30km || 0) * 3 + (b.population100km || 0);
    return popB - popA;
  });
}

/**
 * Get recently erupted volcanoes
 */
export function getRecentlyEruptedVolcanoes(year: number = 2020): GlobalVolcano[] {
  return GLOBAL_VOLCANOES.filter(v => {
    if (!v.lastEruption) return false;
    const eruptionYear = parseInt(v.lastEruption);
    return !isNaN(eruptionYear) && eruptionYear >= year;
  });
}

/**
 * Create URL-friendly slug from volcano name
 */
export function volcanoToSlug(volcano: GlobalVolcano): string {
  return volcano.name.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/(^-|-$)/g, '');
}

/**
 * Get country slug from name
 */
export function countryToSlug(country: string): string {
  return country.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/(^-|-$)/g, '');
}

/**
 * Get country name from slug
 */
export function slugToCountry(slug: string): string | undefined {
  const countries = getVolcanoCountries();
  return countries.find(c => countryToSlug(c) === slug);
}

/**
 * Get global volcano statistics
 */
export function getGlobalVolcanoStats() {
  const volcanoes = GLOBAL_VOLCANOES;
  return {
    total: volcanoes.length,
    active: volcanoes.filter(v => v.status === 'active').length,
    potentiallyActive: volcanoes.filter(v => v.status === 'potentially_active').length,
    holocene: volcanoes.filter(v => v.status === 'holocene').length,
    countries: getVolcanoCountries().length,
    regions: getVolcanoRegions().length,
    recentEruptions: getRecentlyEruptedVolcanoes(2020).length,
    highestRisk: getVolcanoesByPopulationRisk().slice(0, 10),
  };
}