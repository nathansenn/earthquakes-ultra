export function getCountryBySlug(slug: string): SeismicCountry | undefined {
  return seismicCountries.find(c => c.slug === slug);
}

// Get country by code
export function getCountryByCode(code: string): SeismicCountry | undefined {
  return seismicCountries.find(c => c.code === code);
}

// Get countries by risk level
export function getCountriesByRiskLevel(level: SeismicCountry['riskLevel']): SeismicCountry[] {
  return seismicCountries.filter(c => c.riskLevel === level);
}

// Get all country slugs for static generation
export function getAllCountrySlugs(): string[] {
  return seismicCountries.map(c => c.slug);
}

