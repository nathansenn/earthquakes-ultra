// Utility functions
export function getCityBySlug(slug: string): SeismicCity | undefined {
  return majorCities.find(c => c.slug === slug);
}

export function getCitiesByCountry(countryCode: string): SeismicCity[] {
  return majorCities.filter(c => c.countryCode === countryCode);
}

export function getCitiesByPopulation(limit: number = 20): SeismicCity[] {
  return [...majorCities]
    .sort((a, b) => b.population - a.population)
    .slice(0, limit);
}

export function getAllCitySlugs(): string[] {
  return majorCities.map(c => c.slug);
}

export function searchCities(query: string): SeismicCity[] {
  const q = query.toLowerCase();
  return majorCities.filter(c => 
    c.name.toLowerCase().includes(q) ||
    c.country.toLowerCase().includes(q)
  );
}

// Format population for display
export function formatPopulation(pop: number): string {
  if (pop >= 1000000) {
    return `${(pop / 1000000).toFixed(1)}M`;
  }
  if (pop >= 1000) {
    return `${(pop / 1000).toFixed(0)}K`;
  }
  return pop.toString();
}
